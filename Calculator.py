number_1 = int(input('Enter First Number: '))
opt = input('Enter opt: ')
number_2 = int(input('Enter Second Number: '))

if (opt == '+'):
    print(number_1 + number_2)

elif (opt == '-'):
    print(number_1 - number_2)

elif (opt == '*'):
    print(number_1 * number_2)

elif (opt == '/'):
    print(number_1 / number_2)